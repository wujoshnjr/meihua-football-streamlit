def build_payload() -> dict[str, Any]:
    hexagrams = json.loads(HEXAGRAM_PATH.read_text(encoding="utf-8"))
    if set(PROFILES) != set(hexagrams):
        missing = sorted(set(hexagrams) - set(PROFILES))
        extra = sorted(set(PROFILES) - set(hexagrams))
        raise ValueError(f"六十四卦詮釋索引不完整：missing={missing}, extra={extra}")

    entries: dict[str, Any] = {}
    for name, item in sorted(hexagrams.items(), key=lambda pair: int(pair[1]["sequence"])):
        classical_values, football_values = PROFILES[name]
        if len(classical_values) != len(CLASSICAL_FIELDS):
            raise ValueError(f"{name} 傳統卦義欄位數錯誤")
        if len(football_values) != len(FOOTBALL_FIELDS):
            raise ValueError(f"{name} 足球映射欄位數錯誤")
        entries[name] = {
            "sequence": int(item["sequence"]),
            "short_name": str(item["short_name"]),
            "unicode": str(item["unicode"]),
            "classical_meaning": dict(zip(CLASSICAL_FIELDS, classical_values, strict=True)),
            "football_mapping": dict(zip(FOOTBALL_FIELDS, football_values, strict=True)),
        }

    return {
        "version": "zhouyi-64-meaning-football-v1.0.0",
        "language": "zh-Hant",
        "entry_count": 64,
        "classical_fields": list(CLASSICAL_FIELDS),
        "football_fields": list(FOOTBALL_FIELDS),
        "scope": (
            "傳統卦義層依本專案已收錄的卦辭、彖傳、大象、序卦、雜卦與上下卦結構整理；"
            "足球層為本專案的情境映射，只提供閱讀維度，不是《周易》原文、固定吉凶、固定比分或自動預測。"
        ),
        "source_layers": {
            "classical_text": {
                "title": "《周易》經傳",
                "ctext_url": "https://ctext.org/book-of-changes/zh",
                "wikisource_url": "https://zh.wikisource.org/wiki/周易",
                "note": "實際原文由 knowledge/hexagrams.json 與 knowledge/classics/ 提供。",
            },
            "football_mapping": {
                "title": "梅花易數足球情境映射",
                "origin": "本專案原創整理",
                "note": "不以單卦機械決定勝負、總球或比分；必須與體用、動爻、變卦、旺衰及賽前資料合看。",
            },
        },
        "hexagrams": entries,
    }


def main() -> None:
    payload = build_payload()
    OUTPUT_PATH.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"Wrote {OUTPUT_PATH} ({payload['entry_count']} hexagrams)")


if __name__ == "__main__":
    main()
